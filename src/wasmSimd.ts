// Minimal module whose only function returns an i32 extracted from a v128.
// Validation therefore succeeds only when the engine accepts the SIMD opcode set.
const SIMD_PROBE = new Uint8Array([
  0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00,
  0x01, 0x05, 0x01, 0x60, 0x00, 0x01, 0x7f,
  0x03, 0x02, 0x01, 0x00,
  0x0a, 0x19, 0x01, 0x17, 0x00,
  0xfd, 0x0c,
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
  0xfd, 0x1b, 0x00,
  0x0b
]);

export const WASM_SIMD_REQUIRED_MESSAGE = "unsupported: WebAssembly SIMD is required";

export function supportsWasmSimd(
  validate: (bytes: BufferSource) => boolean = WebAssembly.validate
): boolean {
  try {
    return validate(SIMD_PROBE);
  } catch {
    return false;
  }
}
